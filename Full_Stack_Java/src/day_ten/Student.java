package day_ten;
/*day ten (25/06/24)---> Encapsulation
 */
public class Student {
	private int id;
	private String name;
	private int age;
	
	public void setId(int i) {
		id = i;
	}
	
	public int getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
	
	
}
